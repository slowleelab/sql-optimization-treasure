-- good.sql: 布隆过滤器原理演示 - 先检查存在性，再决定是否查库
-- 第一步: 查布隆过滤器（内存操作，纳秒级），判断 ID 是否可能存在
--   如果布隆过滤器说"不存在" -> 一定不存在，直接返回空，不查数据库
--   如果布隆过滤器说"可能存在" -> 可能不存在（误判），才查数据库确认
-- 第二步: 仅当布隆过滤器命中时才查数据库
-- 效果: 99% 以上的无效请求在布隆过滤器层被拦截，数据库零压力

-- 模拟布隆过滤器检查（实际在内存中完成，这里用 SQL 演示原理）
-- id=-1 的哈希值不在布隆过滤器中 -> 直接返回空，不执行下面的查询
SELECT COUNT(*) AS may_exist FROM t_bloom_filter WHERE user_id_hash = -1;
-- 结果: 0 -> 布隆过滤器判定"不存在" -> 直接返回，不查 t_user

-- 仅当布隆过滤器命中时才执行的查询（id=12345 存在于布隆过滤器中）
