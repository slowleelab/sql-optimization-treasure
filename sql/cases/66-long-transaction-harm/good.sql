-- good.sql: 短事务 — 先执行耗时操作（事务外），再开启短事务快速完成
--
-- 优化原理：
--   1. 耗时操作（外部调用）在事务外执行，不持有任何锁
--   2. 事务内只做必要的加锁 + 更新，持锁时间极短（毫秒级）
--   3. 并发事务几乎不会感知到锁等待
--   4. undo log 及时 purge，不会膨胀
--
-- 注意事项：
--   - 外部调用失败时无需回滚数据库（还没开始事务）
--   - 如需保证一致性，可在事务内加状态校验（如乐观锁 version）
--   - 实际项目中应将 RPC/HTTP 调用与数据库事务分离

-- 第1步：先执行耗时操作（在事务外，不持有任何锁）
-- 模拟调用外部支付接口
SELECT SLEEP(5);

-- 第2步：开启短事务，快速完成加锁 + 更新
BEGIN;

-- 加锁并校验（持锁时间仅毫秒级）
SELECT * FROM t_account WHERE id = 1 FOR UPDATE;

-- 扣减余额
UPDATE t_account SET balance = balance - 100 WHERE id = 1;

COMMIT;
